public class Partido extends Pessoa {
	private String Partido;
	private int Numero;
	
	public void setPartido(String Partido) {
		this.Partido = Partido;
	}

	public String getPartido() {
		return Partido;
	}
	
	public void setNumero(int Numero) {
		this.Numero = Numero;
	}

	public int getNumero() {
		return Numero;
	}

}